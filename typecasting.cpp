//Typecasting
// Typecasting is the process of converting a variable from one type to another.
// In C++, you can use static_cast, dynamic_cast, const_cast, and reinterpret_cast
 #include<iostream>
 using namespace std;
int main(){

    int a = 343; // Now this is an integer
    cout <<(float) a << endl; // Typecasting to float

    float b = 34.56;
    cout <<( int) b; //Typecasted to integer







    return 0;
}